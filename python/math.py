import math

n1 = 2
n2 = 10

# Addition
addition = n1 + n2

# Subtraction
subtraction = n1 - n2

# Multiplication
multiplication = n1 * n2

# Divide Quotient
# divide = n1 / n2
divide = n1 // n2

# Percentage Remainder of division
percentage = n1 % n2

# Power
power = n1 ** n2

# floating decimal value
decimalvalue = 4.5 * (n1 * n2)

# squareroot
squareroot = math.sqrt(9)

print(addition)
print(subtraction)
print(multiplication)
print(divide)
print(percentage)
print(power)
print(decimalvalue)
print(squareroot)